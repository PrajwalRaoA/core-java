class Cycle
{
 private String name;
 private int price;
 private int gears;

public void setName(String cycleName)
{
 name=cycleName;
}

public void setPrice(int cyclePrice)
{
 price=cyclePrice;
}

public void setGears(int cycleGears)
{
 gears=cycleGears;
}
public String getName()
{
 return name;
}
public int getPrice()
{
 return price;
}
public int getGears()
{
 return gears;
}
}